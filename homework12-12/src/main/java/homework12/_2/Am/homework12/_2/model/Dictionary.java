package homework12._2.Am.homework12._2.model;

public class Dictionary {
    private String word;
    private String meaning;

    public Dictionary(String word, String meaning) {
        this.word = word;
        this.meaning = meaning;
    }
    public String getWord() {
        return word;
    }
    public String getMeaning() {
        return meaning;
    }
}
